int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;
	int	size;

	size = n;
	i = 0;
	while (i <= size -1)
	{
		if (s1[i] == s2[i])
		{
			i++;
		}
		else if (s1[i] > s2[i])
		{
			return (1);
		}
		else if (s1[i] < s2[i])
		{
			return (-1);
		}
	}
	return (0);
}
